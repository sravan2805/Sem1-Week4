// Variables for Boolean expressions
let a = true;
let b = false;

// AND (&&) operator
let andResult = a && b;
console.log(`(${a} && ${b}) : ${andResult}`);

// OR (||) operator
let orResult = a || b;
console.log(`(${a} || ${b}) : ${orResult}`);

// NOT (!) operator on 'a'
let notAResult = !a;
console.log(`!${a} : ${notAResult}`);

// NOT (!) operator on 'b'
let notBResult = !b;
console.log(`!${b} : ${notBResult}`);
